"""Exercício 2: Soma de Dois Números
● Solicite dois números ao usuário e mostre a soma deles."""
print("\nBem vindo a calculadora dois número e somente adição.\n")
num1 = float(input("\nDigite o primeiro número da soma: "))
num2 = float(input("\nDigite o segundo número da soma: "))
soma = num1+num2
print("\nA sua soma deu: ",soma)